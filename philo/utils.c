#include "philo.h"

int	ft_atoi(char *str)
{
	int	nbr;
	int	i;
	int	sig;

	nbr = 0;
	i = 0;
	sig = 1;
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sig *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr *= 10 + (str[i] - '0');
		i++;
	}
	return (nbr * sig);
}

int	free_data(t_philos *data)
{
	int	i;

	i = -1;
	while (i < data->n)
		pthread_mutex_destroy(&data->forks[i]->fork_mutex);
	free(data->philos);
	free(data->forks);
	return (1);
}

